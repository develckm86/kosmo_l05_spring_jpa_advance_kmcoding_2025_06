/**
 * 정규표현식(RegEx) 기본 개념 정리
 * 	•	정규표현식(Regular Expression)은 문자열 패턴을 검사하거나 검색하기 위한 표현식이다.
 * 	•	이메일, 비밀번호, 전화번호, 숫자 형식 같은 문자열 유효성 검사에 널리 사용된다.
 * 	•	자바스크립트, 자바, 스프링 등 거의 모든 언어/프레임워크에서 동일한 규칙을 가진다.
 *
 * ⸻
 *
 * 정규표현식의 핵심 구성 요소
 *
 * 1. 문자 클래스
 *
 * 특정 문자들만 허용한다.
 * 	•	[0-9] : 숫자
 * 	•	[a-z] : 소문자
 * 	•	[A-Z] : 대문자
 * 	•	[a-zA-Z0-9] : 영문 + 숫자
 * 	•	[^0-9] : 숫자 제외
 *
 * 2. 수량(반복) 지정자
 *
 * 문자가 몇 번 반복되는지 지정.
 * 	•	* : 0번 이상
 * 	•	+ : 1번 이상
 * 	•	? : 0 또는 1번
 * 	•	{2} : 정확히 2번
 * 	•	{2,4} : 2~4번
 * 	•	{2,} : 2번 이상
 *
 * 3. 앵커(문자열 위치 지정)
 *
 * 문자열의 시작과 끝을 의미한다.
 * 	•	^ : 문자열 시작
 * 	•	$ : 문자열 끝
 *
 * 예:
 * ^[0-9]+$ → 전체가 숫자로만 구성되어야 함
 *
 * 4. 특수문자 이스케이프
 *
 * 정규표현식에서 기능을 갖는 문자를 일반 문자로 쓰고 싶을 때 \ 사용.
 *
 * 예:
 * . 은 모든 문자 → \. 으로 쓰면 실제 점(.) 의미
 *
 * ⸻
 *
 * 자주 쓰는 정규식 예시
 *
 * 1. 이메일
 *
 * ^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$
 *
 * 2. 비밀번호(영문, 숫자, 특수문자 포함 8~20)
 *
 * ^(?=.*[A-Za-z])(?=.*\d)(?=.*[!@#$%^&*]).{8,20}$
 *
 * 3. 국내 휴대폰 번호
 *
 * ^01[0-9]-?[0-9]{3,4}-?[0-9]{4}$
 *
 * 4. 숫자만
 *
 * ^[0-9]+$
 *
 * 5. 영문 + 숫자 조합
 *
 * ^[A-Za-z0-9]+$
 *
 *
 * ⸻
 *
 * 정규식의 해석 순서
 * 	1.	^ 로 시작 체크
 * 	2.	문자 패턴 검사
 * 	3.	반복자 확인
 * 	4.	$ 로 끝 체크
 * → 문자열 전체가 규칙에 맞는지 판단
 *
 * ⸻
 *
 * 정규표현식 사용 목적 요약
 * 	•	유효성 검사(Validation)
 * 	•	패턴 검색(Search)
 * 	•	문자열 치환(Replace)
 *
 * 프론트(JS)와 백엔드(Java) 둘 다 동일 개념으로 동작한다.
 *
 * ⸻
 *
 * 자바와 자바스크립트에서 사용 예
 *
 * JavaScript
 *
 * const reg = /^[a-z0-9]+$/;
 * reg.test("abc123"); // true
 *
 * Java
 *
 * boolean result = "abc123".matches("^[a-z0-9]+$");
 */